#include <iostream>
using namespace std;

int main()
{
    int count;
    std::cin >> count;

    int current = 1;

	for (int i = 1; i <= count; i++)
	{
		for (int j = 0; j < count; j++)
		{
			if (j % 2 == 0)
			{
				cout << i + (count * j) << " ";
			}
			else
			{
				cout << (j + 1) * count - (i - 1) << " ";
			}
		}
		cout << endl;
	}
}